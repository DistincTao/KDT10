# 기본형 데이터 타입
# 숫자 (정수, 실수)
a = 123
print(type(a))

b = 3.14
print(type(b))

# Bool 형 (논리형)
c = True
d = False
print(type(c))
print(type(d))

# 문자열 (큰 / 작은 따옴포로 감쌈)

str1 = "대한민국"
str2 = '우리나라'
print(type(str1))
print(type(str2))

str3 = """파이썬
문자열"""
print(str3)

